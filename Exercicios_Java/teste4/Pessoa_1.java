package teste4;

public class Pessoa_1 {

	private String nome;
	private double cpf;
	private int telefone;
	private int idade;
	
	public Pessoa_1 (String nome, int cpf, int telefone, int idade) {
		this.cpf = cpf;
		this.idade = idade;
		this.nome = nome;
		this.telefone = telefone;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public double getCpf() {
		return cpf;
	}

	public void setCpf(int cpf) {
		this.cpf = cpf;
	}

	public int getTelefone() {
		return telefone;
	}

	public void setTelefone(int telefone) {
		this.telefone = telefone;
	}

	public int getIdade() {
		return idade;
	}

	public void setIdade(int idade) {
		this.idade = idade;
	}
	
	

}
